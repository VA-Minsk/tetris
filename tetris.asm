mainGame PROC
	call    RandInit  				; Initialize random numbers



    ret
ENDP
	
RandInit       proc    near
	mov     bp,sp
	sub     sp,4
	mov      ah,02h
	int      1ah
	mov      [bp-2],cl
	mov      [bp-4],dh
	mov     ax,word ptr [bp-2]
	imul    word ptr [bp-4]
	mov     word ptr randseed,ax
	mov     sp,bp
	call    Rand7
	ret
RandInit       endp

Rand7  proc    near
	mov     ax, word ptr randseed
	imul    word ptr randseed

	test    ax,1
	je      short even_number
	add     ax,3172
	mov     word ptr randseed,ax
	jmp     short skip_even
even_number:
	shl		ax,1
	add		ax,Score
skip_even:
	mov     word ptr randseed,ax
	mov     bx,7
	xor     dx,dx
	div     bx
	mov     ax, word ptr OldRand
	mov		word ptr OldRand, dx
	ret
Rand7  endp

end main